// Aula 8 - Alert, Prompt e Confirm

/*
A função alert() no navegador serve para subir um caixa de alerta para o usuario.

A função prompt(), serve como um input, para que o usuario possa preencher com dados, etc.
Tudo que o usuario digitar na função prompt retorna como um tipo String.

A função confirm(), ela serve como um alerta que precisa que o usuario confirme que entendeu a mensagem, caso o usuario click em cancelar retorna um valor boolean false.

 */

alert("Função alert()");

prompt("Digite o seu Nome:");

confirm("Você é maior de 18 anos?");