// Aula 5 - Diferenças entre var e let

/* Variaveis criadas com VAR, podem ser redeclaradas como no exemplo abaixo: */

var nome = "Matheus";
var nome = "Lunkes";
console.log(nome);

// Variaveis com let podem ser reatribuidas mas não redeclaradas como no exemplo abaixo:

let car = 'Fusca';
car = "BMW";
console.log(car);
