// Oque são constantes?
/*
Constantes são variaveis cujo seus valores são imutaveis e indeclaraveis.
EXEMPLO:
*/

const firstName = "Programer";
const lastName = "Developer";

console.log(`${firstName} || ${lastName}`);

// Para criar uma variavel com const, precisamos usar const e usar uma palavra que tenha significado para a referencia de valor da mesma como no exemplo acima, apos isso usamos o sinal de atribuição = para definir o valor desta variavel.

//OBS: variaveis precisam ter nomes significativos, não podemos criar variaveis com palavras reservadas pela linguagem. Não podemos começar variaveis com numeros.
