// Aula 2 - Variaveis com let
// Oque são variaveis e para que servem? 
/*
Variaveis servem para "Salvar" determinado dado/atributo na memoria do computador.
Exemplo:
*/

let nome = "Programer";
let car = 'BMW';

console.log(`I am ${nome}.`);
console.log(car);
// Para criar uma variavel com let, precisamos usar let e usar uma palavra que tenha significado para a referencia de valor da mesma como no exemplo acima, apos isso usamos o sinal de atribuição = para definir o valor desta variavel.

//OBS: variaveis precisam ter nomes significativos, não podemos criar variaveis com palavras reservadas pela linguagem (let let = 'let';). Não podemos começar variaveis com numeros.
